        <hr>
        <p class="texto">La administración de redes informáticas, de redes LAN, redes de área local, o redes 
            de computadoras, son diversas tareas que desarrollan los profesionales en una red 
            informática con el objetivo de brindar de forma eficiente numerosos servicios de red, 
            garantizando la disponibilidad y la calidad que espera el usuario final.</p>
        <p class="texto">Este proyecto nace con el propósito de exponer las necesidades de las redes en la 
